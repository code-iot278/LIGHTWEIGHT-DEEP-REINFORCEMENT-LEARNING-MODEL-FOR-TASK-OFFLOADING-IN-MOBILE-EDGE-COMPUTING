class RLCoatiOptimizer:
    def __init__(self, num_tasks, num_agents, max_iter=100):
        self.num_tasks = num_tasks
        self.num_agents = num_agents
        self.max_iter = max_iter
        self.population_size = 20  # Coati population size
        self.positions = np.random.rand(self.population_size, self.num_tasks)  # solution candidates
        self.best_position = None
        self.best_score = float('inf')

    def fitness(self, solution):
        """
        Define the fitness function to minimize.
        For example, total completion time or cost.
        solution: array representing task allocation.
        """
        # Example: sum of tasks assigned weighted by agent id (dummy)
        score = 0
        for i, task_val in enumerate(solution):
            agent_id = int(task_val * self.num_agents)
            score += (agent_id + 1) * (i + 1)  # dummy calculation
        return score

    def update_positions(self, iteration):
        """
        Update Coati positions based on RL strategy and Coati metaheuristic.
        """
        for i in range(self.population_size):
            # Random walk influenced by best solution (simple exploitation)
            r1 = np.random.rand(self.num_tasks)
            r2 = np.random.rand(self.num_tasks)
            new_position = self.positions[i] + r1 * (self.best_position - abs(self.positions[i])) - r2 * (self.positions[i] - self.best_position)
            # Clip between 0 and 1
            self.positions[i] = np.clip(new_position, 0, 1)

            # Evaluate new position
            score = self.fitness(self.positions[i])
            if score < self.best_score:
                self.best_score = score
                self.best_position = self.positions[i].copy()

    def optimize(self):
        # Initialize best position
        for i in range(self.population_size):
            score = self.fitness(self.positions[i])
            if score < self.best_score:
                self.best_score = score
                self.best_position = self.positions[i].copy()

        # Iterate optimization
        for iter in range(self.max_iter):
            self.update_positions(iter)
            print(f"Iteration {iter+1}/{self.max_iter} - Best Score: {self.best_score:.4f}")

        return self.best_position, self.best_score


