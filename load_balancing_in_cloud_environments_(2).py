"""# **Communicate to one vm machine to another vm machine**"""

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from matplotlib.lines import Line2D

# -----------------------------
# Load Data
# -----------------------------
vm_df = pd.read_csv("vm_workloads.csv")
node_df = pd.read_csv("federated_node_output.csv")

vm_df.columns = vm_df.columns.str.strip()
node_df.columns = node_df.columns.str.strip()

# Add extra workload
np.random.seed(42)
vm_df["Extra_Work"] = np.random.randint(5, 26, size=len(vm_df))
vm_df["Total_Workload"] = vm_df["Workload"] + vm_df["Extra_Work"]
node_df["Assigned_Workload"] = 0

# -----------------------------
# RL-Coati Parameters
# -----------------------------
alpha = 0.6
beta = 0.4
gamma = 0.2

def normalize(col):
    return (col - col.min()) / (col.max() - col.min() + 1e-5)

node_df["CPU_norm"] = normalize(node_df["CPU(%)"])
node_df["RAM_norm"] = normalize(node_df["RAM(MB)"])
node_df["Latency_norm"] = normalize(-node_df["Latency(ms)"])  # lower is better

# -----------------------------
# RL-Coati Assignment
# -----------------------------
assigned_tasks = []

for _, vm in vm_df.iterrows():
    cloud = vm["Cloud"]
    vm_id = vm["VM"]
    total_work = int(vm["Total_Workload"])

    candidates = node_df[node_df["Cloud"] == cloud].copy()
    if candidates.empty:
        print(f"No nodes in {cloud} for {vm_id}")
        continue

    candidates["GreedyScore"] = (
        0.5 * candidates["CPU_norm"] +
        0.4 * candidates["RAM_norm"] +
        0.1 * candidates["Latency_norm"]
    )

    prob = np.exp(beta * candidates["GreedyScore"])
    prob /= prob.sum()

    if np.random.rand() < alpha:
        selected = candidates.sort_values("GreedyScore", ascending=False).iloc[0]
    else:
        selected = candidates.sample(weights=prob).iloc[0]

    if np.random.rand() < gamma:
        selected = candidates.sample(1).iloc[0]

    assigned_tasks.append({
        "Cloud": cloud,
        "VM": vm_id,
        "Original_Workload": vm["Workload"],
        "Extra_Work": vm["Extra_Work"],
        "Total_Workload": total_work,
        "Assigned_Node_X": selected["X"],
        "Assigned_Node_Y": selected["Y"],
        "Node_CPU(%)": selected["CPU(%)"],
        "Node_RAM(MB)": selected["RAM(MB)"],
        "Node_Latency(ms)": selected["Latency(ms)"]
    })

    node_df.loc[selected.name, "Assigned_Workload"] += total_work

assigned_df = pd.DataFrame(assigned_tasks)
assigned_df.to_csv("assigned_vm_tasks_rl_coati.csv", index=False)
print("✅ Saved: assigned_vm_tasks_rl_coati.csv")

