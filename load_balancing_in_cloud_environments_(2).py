import flwr as fl
import numpy as np
import tensorflow as tf
from tensorflow.keras import layers, models
import matplotlib.pyplot as plt
import pandas as pd
from matplotlib.patches import Patch
from matplotlib.font_manager import FontProperties
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score

# ==========================================================
# 1. Build CNN–LSTM–Attention Model
# ==========================================================
def build_cnn_lstm_attention_model(time_steps=10, img_height=32, img_width=32, channels=1):
    input_shape = (time_steps, img_height, img_width, channels)

    cnn = models.Sequential([
        layers.Input(shape=(img_height, img_width, channels)),
        layers.Conv2D(16, (3, 3), activation='relu'),
        layers.MaxPooling2D((2, 2)),
        layers.Conv2D(32, (3, 3), activation='relu'),
        layers.Flatten(),
    ])

    inputs = layers.Input(shape=input_shape)
    x = layers.TimeDistributed(cnn)(inputs)
    x = layers.LSTM(64, return_sequences=True)(x)

    # Attention
    attention = layers.Dense(1, activation='tanh')(x)
    attention = layers.Flatten()(attention)
    attention = layers.Activation('softmax')(attention)
    attention = layers.RepeatVector(64)(attention)
    attention = layers.Permute([2, 1])(attention)
    x = layers.multiply([x, attention])
    x = layers.Lambda(lambda z: tf.reduce_sum(z, axis=1))(x)

    x = layers.Dense(32, activation='relu')(x)
    outputs = layers.Dense(3)(x)
    model = models.Model(inputs=inputs, outputs=outputs)
    model.compile(optimizer='adam', loss='mse', metrics=['mae'])
    return model


# ==========================================================
# 2. Generate Synthetic Data
# ==========================================================
def generate_data(samples=30):
    X = np.random.rand(samples, 10, 32, 32, 1).astype(np.float32)
    y = np.random.rand(samples, 3) * np.array([100, 1024, 200])
    y = y.astype(np.float32)
    return X, y


# ==========================================================
# 3. Flower FL Client
# ==========================================================
def create_client(cid):
    class FLClient(fl.client.NumPyClient):
        def __init__(self):
            self.model = build_cnn_lstm_attention_model()
            self.X, self.y = generate_data(samples=30)

        def get_parameters(self, config):
            return self.model.get_weights()

        def fit(self, parameters, config):
            self.model.set_weights(parameters)
            self.model.fit(self.X, self.y, epochs=2, batch_size=4, verbose=0)
            return self.model.get_weights(), len(self.X), {}

        def evaluate(self, parameters, config):
            self.model.set_weights(parameters)
            loss, mae = self.model.evaluate(self.X, self.y, verbose=0)
            return loss, len(self.X), {"mae": mae}

    return FLClient()


def binarize_cpu(arr, threshold=70):
    return (arr[:, 0] > threshold).astype(int)


