# Function to randomly distribute node counts among types
def random_node_distribution(total_nodes, num_types):
    rand_vals = np.random.rand(num_types)
    proportions = rand_vals / rand_vals.sum()
    counts = np.floor(proportions * total_nodes).astype(int)
    while counts.sum() < total_nodes:
        counts[np.argmin(counts)] += 1
    return counts

